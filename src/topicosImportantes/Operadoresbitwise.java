package topicosImportantes;

public class Operadoresbitwise {
    public static void main(String[] args) {
        int n1 = 89;
        int n2 = 60;

        //e bit a bit
        System.out.println(n1 & n2);
        //ou bit a bit
        System.out.println(n1 | n2);
        //xor bit a bit
        System.out.println(n1 ^ n2);
    }
}
