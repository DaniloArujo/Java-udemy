package estruturaCondicional;

public class EstruturaCondicional{
    public static void main(String[] args){
        int x = 5;

        if(x < 5){
            System.out.println("Bom dia!");
        }

        if(x == 5){
            System.out.println("Boa tarde!");
        }

        if(x > 5){
            System.out.println("Boa noite!");
        }
    }
}